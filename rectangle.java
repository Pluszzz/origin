public class rectangle {
        public int kuan;
        public int height;
        int getkuan() {
            return kuan;
        }
        int getheight() {
            return height;
        }
        public int rectangle_area() {
            return kuan * height;  
        }
        public  int rectangle_preimeter() {
            return 2 *(kuan+height);
    }

}
    

